# Listas em Python.

# Criação de uma lista para armazenas 7 notas de alunos.
lista_notas = [7.5, 8.3, 9.4, 10.0, 9.5, 4.7, 5.5]

# Exibir a lista (forma mais simples)
print(lista_notas)

# Exibir o quarto elemento da lista
print(f"Quarto elemento da lista: {lista_notas[3]}")

# Exibir o último elemento da lista (2 formas)
print(f"Último elemento da lista: {lista_notas[6]}")

# Outra forma de acessar o último elemento da lista
print(f"Último elemento da lista: {lista_notas[-1]}")

# Exibir os 3 primeiros elementos da lista (notação slice)
print(f"Três primeiros elementos da lista: {lista_notas[0:3]}")
