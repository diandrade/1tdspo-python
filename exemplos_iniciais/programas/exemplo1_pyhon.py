# Criação de variáveis no Python

# Criação de uma variável numérico inteira
numero_inteiro = 15

# Função de Saída de dados no Python (print)
print(numero_inteiro)

# Criação de uma variável numérico real (float)
nota_aluno = 8.7

# Mostrar o conteúdo da variável nota_aluno
print(nota_aluno)

# Mostrar o tipo de dado da variável nota_aluno
print(type(nota_aluno))

# Mostrar mensagem de texto junto com variável
print(f"A nota do aluno é {nota_aluno}")