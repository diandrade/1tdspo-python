# Operações aritméticas no Python

num1 = 8
num2 = 3

# Adição
soma = num1 + num2

print(f"A soma dos números é {soma}")

# Subtração
subtracao = num1 - num2

print(f"A subtração dos números é {subtracao}")

# Multiplicação
multiplicacao = num1 * num2

print(f"A multiplicação dos números é {multiplicacao}")

# Divisão
divisao = num1 / num2

print(f"A divisão dos números é {divisao}")

# Limitar as casas decimais (exemplo: 2 casas decimais)
print(f"A divisão com 2 casas decimais é {divisao:.2f}")

# Exponenciação
num1_cubo = num1 ** 3

print(f"{num1} elevado ao cubo é {num1_cubo}")

# Resto da Divisão

resto_divisao = num1 % 2

print(f"O resto da divisão de {num1} por 2 é {resto_divisao}")