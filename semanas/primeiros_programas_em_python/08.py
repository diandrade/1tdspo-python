'''
Escreva um programa que solicite três valores inteiros
(representados pelas variáveis A, B e C),
calcule e apresente como resultado final
o valor da soma dos quadrados dos três valores lidos.
'''

a = int(input("Insira o valor de A: "))
b = int(input("Insira o valor de B: "))
c = int(input("Insira o valor de C: "))

soma_dos_quadrados = a ** 2 + b ** 2 + c ** 2

print(f"O valor da soma dos quadrados é: {soma_dos_quadrados}")
