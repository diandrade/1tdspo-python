'''
Escreva um programa que solicite 2 números inteiros,
faça a soma, subtração e multiplicação.
Por fim, mostre os resultados.
'''

num1 = float(input("Digite o primeiro número inteiro: "))
num2 = float(input("Digite o segundo número inteiro: "))

soma = num1 + num2
subtracao = num1 - num2
multiplicacao = num1 * num2

print(f"A soma, subtração e divisão, em sequência são: {soma} , {subtracao} , {multiplicacao}")
