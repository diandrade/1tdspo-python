'''
Exemplo: Mostrar se um aluno foi aprovado ou reprovado de acordo  com a média (> = 7 aprovado / < 7 reprovado)
'''
media = float(input("Digite a média do aluno: "))

if (media >= 7.0):
    print("O aluno foi aprovado")
else:
    print("O aluno foi reprovado")

