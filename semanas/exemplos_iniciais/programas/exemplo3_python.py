# Função para entrada de dados no Python (input)

# Entrada de uma variável texto
nome_pessoa = input("Digite o nome da pessoa: ")

print(f"O nome da pessoa é {nome_pessoa}")

# Entrada de uma variável numérica inteira
idade_pessoa = int(input("Digite a idade da pessoa: "))

print(type(idade_pessoa))

print(f"A idade da pessoa é {idade_pessoa}")

# Entrada de uma variável numérica real (float)

salario_funcionario = float(input("Digite o salário do funcionário: "))

print(f"O salário do funcionário é {salario_funcionario:.2f}")

