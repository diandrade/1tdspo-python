'''
Escreva um programa que solicite 3 lados de um triângulo,
calcule e mostre seu perímetro (soma dos lados)
'''

lado1 = int(input("Insira o primeiro lado do triângulo: "))
lado2 = int(input("Insira o segundo lado do triângulo: "))
lado3 = int(input("Insira o terceiro lado do triângulo: "))
print(f"O perímetro do triângulo é: {lado1 + lado2 + lado3}")
